import java.util.*;
public class Alphabets{
    public static void main(String args[]){
        char alphabet = 'A';
        for (int i=1;i<=26;i++){
            System.out.print(alphabet+" ");
            alphabet++;
        }System.out.println();
        char alphabets = 'a';
        for (int i=1;i<=26;i++){
            System.out.print(alphabets+" ");
            alphabets++;
        }System.out.println();
    }
}